const SIDEBAR_COOKIE_NAME = "sidebar_state"
const SIDEBAR_RESIZABLE_WIDTH_COOKIE_NAME = "sidebar_resizable_width";
const SIDEBAR_COOKIE_MAX_AGE = 60 * 60 * 24 * 7
const SIDEBAR_DEFAULT_WIDTH = "16rem" // Default expanded width
const SIDEBAR_WIDTH_MOBILE = "18rem"
const SIDEBAR_WIDTH_ICON = "3rem" // Collapsed icon mode width
const SIDEBAR_MIN_WIDTH = 150; // px
const SIDEBAR_MAX_WIDTH = 500; // px
const SIDEBAR_KEYBOARD_SHORTCUT = "b"

type SidebarCollapsibleMode = "offcanvas" | "icon" | "none";

type SidebarContextType = {
  state: "expanded" | "collapsed"
  open: boolean
  setOpen: (open: boolean) => void
  openMobile: boolean
  setOpenMobile: (open: boolean) => void
  isMobile: boolean | undefined
  toggleSidebar: () => void
  resizableWidth: string;
  setResizableWidth: (width: string) => void;
  isResizing: boolean;
  setIsResizing: (isResizing: boolean) => void;
  collapsibleMode: SidebarCollapsibleMode;
  side: "left" | "right";
}

const SidebarContext = React.createContext<SidebarContextType | null>(null)

function useSidebar() {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }
  return context
}

const SidebarProvider = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & {
    defaultOpen?: boolean
    open?: boolean
    onOpenChange?: (open: boolean) => void
    collapsible?: SidebarCollapsibleMode // Added to provider props
    defaultResizableWidth?: string // Allow overriding default resizable width
    side?: "left" | "right";
  }
>(
  (
    {
      defaultOpen = true,
      open: openProp,
      onOpenChange: setOpenProp,
      collapsible = "offcanvas", // Default collapsible mode
      defaultResizableWidth,
      side = "left",
      className,
      style,
      children,
      ...props
    },
    ref
  ) => {
    const isMobile = useIsMobile()
    const [openMobile, setOpenMobile] = React.useState(false)
    const [_open, _setOpen] = React.useState(defaultOpen)
    const open = openProp ?? _open

    const [resizableWidth, _setResizableWidth] = React.useState(
      () => {
        if (typeof window !== 'undefined') {
          return localStorage.getItem(SIDEBAR_RESIZABLE_WIDTH_COOKIE_NAME) || defaultResizableWidth || SIDEBAR_DEFAULT_WIDTH;
        }
        return defaultResizableWidth || SIDEBAR_DEFAULT_WIDTH;
      }
    );
    const [isResizing, setIsResizing] = React.useState(false);

    const setResizableWidth = React.useCallback((newWidth: string) => {
      _setResizableWidth(newWidth);
      if (typeof window !== 'undefined') {
        localStorage.setItem(SIDEBAR_RESIZABLE_WIDTH_COOKIE_NAME, newWidth);
      }
    }, []);
    
    React.useEffect(() => {
      if (isResizing) {
        document.body.classList.add("sidebar-resizing");
      } else {
        document.body.classList.remove("sidebar-resizing");
      }
    }, [isResizing]);


    const setOpen = React.useCallback(
      (value: boolean | ((value: boolean) => boolean)) => {
        const openState = typeof value === "function" ? value(open) : value
        if (setOpenProp) {
          setOpenProp(openState)
        } else {
          _setOpen(openState)
        }

        if (typeof document !== 'undefined') {
            document.cookie = `${SIDEBAR_COOKIE_NAME}=${openState}; path=/; max-age=${SIDEBAR_COOKIE_MAX_AGE}`
        }
      },
      [setOpenProp, open]
    )

    const toggleSidebar = React.useCallback(() => {
      if (isMobile === undefined) return; 
      return isMobile
        ? setOpenMobile((current) => !current)
        : setOpen((current) => !current)
    }, [isMobile, setOpen, setOpenMobile])

    React.useEffect(() => {
      const handleKeyDown = (event: KeyboardEvent) => {
        if (
          event.key === SIDEBAR_KEYBOARD_SHORTCUT &&
          (event.metaKey || event.ctrlKey)
        ) {
          event.preventDefault()
          toggleSidebar()
        }
      }
      if (typeof window !== 'undefined') {
        window.addEventListener("keydown", handleKeyDown)
        return () => window.removeEventListener("keydown", handleKeyDown)
      }
    }, [toggleSidebar])
    
    React.useEffect(() => {
      if (typeof document !== 'undefined') {
        const cookieValue = document.cookie
          .split('; ')
          .find(row => row.startsWith(`${SIDEBAR_COOKIE_NAME}=`))
          ?.split('=')[1];
        if (cookieValue) {
          _setOpen(cookieValue === 'true');
        }
      }
    }, []);


    const state = open ? "expanded" : "collapsed"

    const contextValue = React.useMemo<SidebarContextType>(
      () => ({
        state,
        open,
        setOpen,
        isMobile,
        openMobile,
        setOpenMobile,
        toggleSidebar,
        resizableWidth,
        setResizableWidth,
        isResizing,
        setIsResizing,
        collapsibleMode: collapsible,
        side,
      }),
      [state, open, setOpen, isMobile, openMobile, setOpenMobile, toggleSidebar, resizableWidth, setResizableWidth, isResizing, setIsResizing, collapsible, side]
    )
    
    const currentSidebarWidth = (!isMobile && open) ? resizableWidth : SIDEBAR_DEFAULT_WIDTH;


    return (
      <SidebarContext.Provider value={contextValue}>
        <TooltipProvider delayDuration={0}>
          <div
            style={
              {
                "--sidebar-width-expanded": currentSidebarWidth,
                "--sidebar-width-collapsed": SIDEBAR_WIDTH_ICON,
                "--sidebar-width-mobile": SIDEBAR_WIDTH_MOBILE,
                ...style,
              } as React.CSSProperties
            }
            className={cn(
              "group/sidebar-wrapper flex min-h-svh w-full has-[[data-variant=inset]]:bg-sidebar", // This is the main flex container
              className
            )}
            ref={ref}
            {...props}
          >
            {children}
          </div>
        </TooltipProvider>
      </SidebarContext.Provider>
    )
  }
)
SidebarProvider.displayName = "SidebarProvider"


const ResizeHandle = () => {
  const { resizableWidth, setResizableWidth, setIsResizing, side } = useSidebar();
  const handleRef = React.useRef<HTMLDivElement>(null);

  const onMouseDown = React.useCallback((e: React.MouseEvent<HTMLDivElement>) => {
    e.preventDefault();
    setIsResizing(true);

    const startX = e.clientX;
    // Get the sidebar element which is the direct parent of the resize handle if we move it inside
    const sidebarElement = handleRef.current?.parentElement; 
    if (!sidebarElement) return;
    
    const initialPixelWidth = sidebarElement.getBoundingClientRect().width;

    const onMouseMove = (moveEvent: MouseEvent) => {
      const currentX = moveEvent.clientX;
      let deltaX = currentX - startX;
      if (side === 'right') { 
        deltaX = -deltaX;
      }
      
      let newPixelWidth = initialPixelWidth + deltaX;
      
      newPixelWidth = Math.max(SIDEBAR_MIN_WIDTH, Math.min(newPixelWidth, SIDEBAR_MAX_WIDTH));
      setResizableWidth(`${newPixelWidth}px`);
    };

    const onMouseUp = () => {
      setIsResizing(false);
      document.removeEventListener("mousemove", onMouseMove);
      document.removeEventListener("mouseup", onMouseUp);
    };

    document.addEventListener("mousemove", onMouseMove);
    document.addEventListener("mouseup", onMouseUp);
  }, [setResizableWidth, setIsResizing, side]);

  return (
    <div 
      ref={handleRef} 
      onMouseDown={onMouseDown} 
      className={cn(
        "absolute top-0 bottom-0 w-1.5 bg-transparent group-hover/sidebar:bg-sidebar-border/30 hover:!bg-sidebar-border/80 transition-colors duration-150 z-20", // z-20 to be above content
        side === 'left' ? "right-0 cursor-col-resize" : "left-0 cursor-col-resize"
      )}
      title="Resize sidebar"
    />
  );
}


const Sidebar = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div">
>(
  (
    {
      className,
      children,
      ...props
    },
    ref
  ) => {
    const { isMobile, state, openMobile, setOpenMobile, collapsibleMode, side, resizableWidth } = useSidebar()

    if (isMobile === undefined) {
      return null; // Or some skeleton/loader
    }

    if (isMobile) {
      return (
        <Sheet open={openMobile} onOpenChange={setOpenMobile}>
          <SheetContent
            data-sidebar="sidebar"
            data-mobile="true"
            className="w-[var(--sidebar-width-mobile)] bg-sidebar p-0 text-sidebar-foreground [&>button]:hidden"
            side={side}
          >
            <div className="flex h-full w-full flex-col">{children}</div>
          </SheetContent>
        </Sheet>
      )
    }

    // Desktop rendering (isMobile is false) - In-flow layout
    return (
      <div
        ref={ref}
        className={cn(
          "group/sidebar hidden md:flex flex-col text-sidebar-foreground relative bg-sidebar transition-all duration-200 ease-linear h-svh", // In-flow, takes full height
          // Width classes based on state and collapsible mode
          collapsibleMode === "icon" && state === "collapsed"
            ? "w-[var(--sidebar-width-collapsed)]" // p-0 is handled by SidebarContent in icon mode
            : "w-[var(--sidebar-width-expanded)]",
          // Border based on side
          side === "left" ? "border-r border-sidebar-border" : "border-l border-sidebar-border",
          className
        )}
        style={{
          width: (collapsibleMode === "icon" && state === "collapsed")
            ? 'var(--sidebar-width-collapsed)' // Fixed width for icon mode collapsed
            : resizableWidth, // Dynamic resizable width when expanded
        }}
        data-state={state}
        data-collapsible={collapsibleMode}
        data-side={side}
        {...props}
      >
        {children}
        {state === "expanded" && collapsibleMode !== "none" && <ResizeHandle />}
      </div>
    )
  }
)
Sidebar.displayName = "Sidebar"

